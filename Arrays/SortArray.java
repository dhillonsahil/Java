package Arrays;
import java.util.Arrays;

public class SortArray {
    public static void main(String[] args) {
        int n[]={5,3,7};
        Arrays.sort(n);
        for(int a:n){
         System.out.println(a);
     }
     char ch[] = {'s','a','h'};
     Arrays.sort(ch);
     for(char a:ch){
         System.out.println(a);
     }
     }
}
